def esc(value):
    """Escape text for ReportLab paragraph markup; lift **bold** to <b>."""
    if value is None:
        return ''
    text = html.escape(str(value), quote=False)
    while '**' in text:
        text = text.replace('**', '<b>', 1)
        if '**' in text:
            text = text.replace('**', '</b>', 1)
        else:
            text += '</b>'
    return text


def fmt_pct(value):
    if value is None:
        return '—'
    try:
        return f'{round(float(value))}%'
    except (TypeError, ValueError):
        return str(value)


def fmt_dt(iso):
    if not iso:
        return '—'
    try:
        return datetime.fromisoformat(str(iso).replace('Z', '+00:00')).strftime('%Y-%m-%d %H:%M UTC')
    except ValueError:
        return str(iso)


