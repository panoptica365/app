def esc(value):
    """Escape text for ReportLab paragraph markup; lift **bold** to <b>."""
    if value is None:
        return ''
    text = html.escape(str(value), quote=False)
    while '**' in text:
        text = text.replace('**', '<b>', 1)
        if '**' in text:
            text = text.replace('**', '</b>', 1)
        else:
            text += '</b>'
    return text


def fmt_pct(value):
    if value is None:
        return '—'
    try:
        return f'{round(float(value))}%'
    except (TypeError, ValueError):
        return str(value)


def fmt_dt(iso):
    if not iso:
        return '—'
    try:
        return datetime.fromisoformat(str(iso).replace('Z', '+00:00')).strftime('%Y-%m-%d %H:%M UTC')
    except ValueError:
        return str(iso)


def find_cover_image():
    """Canonical Panoptica365 cover; fall back to the repo-root asset."""
    for candidate in (
        os.path.join(PROJECT_ROOT, 'public', 'img', 'report-cover.png'),
        os.path.join(PROJECT_ROOT, 'Panoptica365 Report Cover.png'),
    ):
        if os.path.exists(candidate):
            return candidate
    return None


