# ─── Charts (matplotlib, headless) ───

def chart_score_gauge(score_pct, buf):
    """Donut gauge for the Microsoft Secure Score. Returns False if no data."""
    if score_pct is None:
        return False
    try:
        score = float(score_pct)
    except (TypeError, ValueError):
        return False

    fig, ax = plt.subplots(figsize=(2.6, 2.6))
    ring = COLORS['good'] if score >= 70 else COLORS['medium'] if score >= 45 else COLORS['high']
    ax.pie([score, max(0.0, 100 - score)], colors=[ring, '#E8E8E8'],
           startangle=90, counterclock=False,
           wedgeprops=dict(width=0.32, edgecolor='white', linewidth=2))
    ax.text(0, 0, f'{round(score)}%', ha='center', va='center',
            fontsize=26, fontweight='bold', color=COLORS['primary'])
    ax.set(aspect='equal')
    fig.savefig(buf, format='png', dpi=150, bbox_inches='tight',
                facecolor='white', edgecolor='none')
    plt.close(fig)
    return True


def chart_findings_donut(counts, buf, S):
    """Donut of findings by priority (high/medium/low). False if no findings."""
    order = [('high', S['prio_high'], COLORS['high']),
             ('medium', S['prio_medium'], COLORS['medium']),
             ('low', S['prio_low'], COLORS['low'])]
    values, colors_list = [], []
    for key, _label, color in order:
        cnt = counts.get(key, 0)
        if cnt > 0:
            values.append(cnt)
            colors_list.append(color)
    total = sum(values)
    if total == 0:
        return False

    fig, ax = plt.subplots(figsize=(2.6, 2.6))
    ax.pie(values, colors=colors_list, startangle=90, counterclock=False,
           wedgeprops=dict(width=0.32, edgecolor='white', linewidth=2))
    ax.text(0, 0.07, str(total), ha='center', va='center',
            fontsize=24, fontweight='bold', color=COLORS['primary'])
    ax.text(0, -0.20, S['findings_unit'], ha='center', va='center',
            fontsize=8, color=COLORS['text_light'])
    ax.set(aspect='equal')
    fig.savefig(buf, format='png', dpi=150, bbox_inches='tight',
                facecolor='white', edgecolor='none')
    plt.close(fig)
    return True


def prose(text, styles):
    """Split a narrative string on blank lines into separate paragraphs."""
    flows = []
    if not text:
        return flows
    for chunk in [c.strip() for c in str(text).replace('\r', '').split('\n\n') if c.strip()]:
        flows.append(Paragraph(esc(chunk).replace('\n', '<br/>'), styles['Body']))
    return flows


def section_header(label, styles, width):
    """A full-width colored section bar."""
    tbl = Table([[Paragraph(esc(label), styles['Section'])]], colWidths=[width])
    tbl.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), HexColor(COLORS['primary'])),
        ('LEFTPADDING', (0, 0), (-1, -1), 10),
        ('RIGHTPADDING', (0, 0), (-1, -1), 10),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]))
    return tbl


def section(label, content_flows, styles, width):
    """A section: the header bar is glued to its first content flowable via
    KeepTogether, so a header can never be orphaned at the foot of a page."""
    header = section_header(label, styles, width)
    if not content_flows:
        return [header, Spacer(1, 12)]
    head_group = KeepTogether([header, Spacer(1, 8), content_flows[0]])
    return [head_group] + content_flows[1:] + [Spacer(1, 12)]


def priority_label(priority, S):
    p = (priority or '').lower()
    if p == 'high':
        return S['prio_high'], COLORS['high']
    if p == 'low':
        return S['prio_low'], COLORS['low']
    return S['prio_medium'], COLORS['medium']


def gap_flowables(gap, styles, S):
    """The raw flowables for one finding — title + priority tag, detail, and
    the optional template line. Returned as a flat list so callers can wrap
    them in a single (non-nested) KeepTogether."""
    label, color = priority_label(gap.get('priority'), S)
    head = (f'<font color="{color}"><b>[{esc(label)}]</b></font> '
            f'<b>{esc(gap.get("title") or "")}</b>')
    out = [Paragraph(head, styles['Gap'])]
    if gap.get('detail'):
        out.append(Paragraph(esc(gap.get('detail')), styles['Gap']))
    tpl = gap.get('deployable_template')
    if tpl:
        out.append(Paragraph(
            f'&#10003; {esc(S["deployable"])} — {esc(S["tpl"])}: <b>{esc(tpl)}</b>',
            styles['GapMeta']))
    else:
        out.append(Spacer(1, 4))
    return out


def render_area(area, styles, S):
    """Render a CA / Intune / Security-Settings section's content flowables.
    Each finding is one atomic KeepTogether and the "Gaps" sub-heading is
    flat-glued to the first finding — no nested KeepTogethers, which would
    mis-measure and strand whitespace at page ends."""
    flows = []
    flows.extend(prose((area or {}).get('narrative'), styles))

    strengths = (area or {}).get('strengths') or []
    if strengths:
        bullets = [
            Paragraph(f'<font color="{COLORS["good"]}">&#10003;</font> {esc(item)}', styles['Bullet'])
            for item in strengths
        ]
        flows.append(KeepTogether([Paragraph(esc(S['sub_working']), styles['SubHead']), bullets[0]]))
        flows.extend(bullets[1:])

    gaps = (area or {}).get('gaps') or []
    gaps_subhead = Paragraph(esc(S['sub_gaps']), styles['SubHead'])
    if gaps:
        # First finding flat-glued to the sub-heading; each finding atomic.
        flows.append(KeepTogether([gaps_subhead] + gap_flowables(gaps[0], styles, S)))
        for gap in gaps[1:]:
            flows.append(KeepTogether(gap_flowables(gap, styles, S)))
    else:
        flows.append(KeepTogether([gaps_subhead, Paragraph(esc(S['none']), styles['Body'])]))
    return flows


def count_findings_by_priority(analysis):
    counts = {'high': 0, 'medium': 0, 'low': 0}
    for key in ('conditional_access', 'intune', 'security_settings'):
        for gap in (analysis.get(key) or {}).get('gaps') or []:
            p = (gap.get('priority') or 'medium').lower()
            if p not in counts:
                p = 'medium'
            counts[p] += 1
    return counts


def make_cover(S, tenant_name, generated_iso, cover_path):
    """First-page callback — full-bleed cover image + title text."""
    def _cover(canvas, doc):
        canvas.saveState()
        w, h = letter
        if cover_path:
            canvas.drawImage(cover_path, 0, 0, width=w, height=h,
                             preserveAspectRatio=True, anchor='c')
        canvas.setFont('Helvetica-Bold', 30)
        canvas.setFillColor(HexColor('#1A2A3A'))
        canvas.drawCentredString(w / 2, h * 0.79, S['title'])
        canvas.setFont('Helvetica', 13)
        canvas.setFillColor(HexColor('#4A5568'))
        canvas.drawCentredString(w / 2, h * 0.745, S['subtitle'])
        canvas.setFont('Helvetica-Bold', 17)
        canvas.setFillColor(HexColor(COLORS['primary']))
        canvas.drawCentredString(w / 2, h * 0.69, tenant_name or '—')
        canvas.setFont('Helvetica', 10)
        canvas.setFillColor(HexColor('#666666'))
        canvas.drawCentredString(w / 2, h * 0.655,
                                 f"{S['generated']}: {fmt_dt(generated_iso)}")
        canvas.restoreState()
    return _cover


def make_footer(S, msp_name, attribution_on):
    """Later-pages callback — thin rule + attribution + page number."""
    def _footer(canvas, doc):
        canvas.saveState()
        canvas.setStrokeColor(HexColor(COLORS['rule']))
        canvas.setLineWidth(0.5)
        canvas.line(0.75 * inch, 0.6 * inch, 7.75 * inch, 0.6 * inch)
        canvas.setFont('Helvetica', 7.5)
        canvas.setFillColor(HexColor(COLORS['text_light']))
        left = msp_name if msp_name else (S['attribution'] if attribution_on else '')
        if left:
            canvas.drawString(0.75 * inch, 0.42 * inch, left)
        canvas.drawRightString(7.75 * inch, 0.42 * inch, f"{S['page']} {doc.page}")
        canvas.restoreState()
    return _footer


def main():
    if len(sys.argv) < 3:
        sys.stderr.write('Usage: generate-quick-assessment-report.py <input.json> <output.pdf>\n')
        sys.exit(1)

    with open(sys.argv[1], 'r', encoding='utf-8') as fh:
        data = json.load(fh)
    output_path = sys.argv[2]

    lang = data.get('language', 'en')
    S = STRINGS.get(lang, STRINGS['en'])
    styles = build_styles()
    analysis = data.get('analysis') or {}
    tenant = data.get('tenant') or {}
    headline = data.get('headline') or {}
    report_config = data.get('reportConfig') or {}
    msp_name = report_config.get('mspName') or ''
    attribution_on = report_config.get('platformAttribution', True)
    width = letter[0] - 1.5 * inch  # matches the 0.75" left/right margins

    story = []

    # ─── Page 2: meta line ───
    story.append(Paragraph(
        f"<b>{esc(S['tenant'])}:</b> {esc(tenant.get('display_name', '—'))}"
        f" &nbsp;|&nbsp; {esc(S['captured'])}: {esc(fmt_dt(data.get('capturedAt')))}",
        styles['Meta']))
    story.append(Spacer(1, 10))

    # ─── Headline cards ───
    cards = [
        (fmt_pct(headline.get('secure_score')), S['hl_secure']),
        (fmt_pct(headline.get('mfa_pct')), S['hl_mfa']),
        (str(headline.get('ca_policy_count')) if headline.get('ca_policy_count') is not None else '—', S['hl_ca']),
        (str(headline.get('security_settings_total')) if headline.get('security_settings_total') is not None else '—', S['hl_settings']),
    ]
    card_row = [
        Table([[Paragraph(esc(v), styles['CardValue'])], [Paragraph(esc(l), styles['CardLabel'])]],
              colWidths=[width / 4.0 - 6])
        for (v, l) in cards
    ]
    card_tbl = Table([card_row], colWidths=[width / 4.0] * 4)
    card_tbl.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), HexColor(COLORS['band'])),
        ('TOPPADDING', (0, 0), (-1, -1), 10),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 10),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
    ]))
    story.append(card_tbl)
    story.append(Spacer(1, 14))

    # ─── Charts: Secure Score gauge + Findings by priority ───
    chart_cells = []
    score_buf = BytesIO()
    if chart_score_gauge(headline.get('secure_score'), score_buf):
        score_buf.seek(0)
        chart_cells.append([
            RLImage(score_buf, width=1.9 * inch, height=1.9 * inch),
            S['chart_score'],
        ])
    findings_buf = BytesIO()
    if chart_findings_donut(count_findings_by_priority(analysis), findings_buf, S):
        findings_buf.seek(0)
        chart_cells.append([
            RLImage(findings_buf, width=1.9 * inch, height=1.9 * inch),
            S['chart_findings'],
        ])
    if chart_cells:
        col_w = width / len(chart_cells)
        cell_tables = [
            Table([[img], [Paragraph(esc(caption), styles['Caption'])]], colWidths=[col_w])
            for (img, caption) in chart_cells
        ]
        charts_tbl = Table([cell_tables], colWidths=[col_w] * len(chart_cells))
        charts_tbl.setStyle(TableStyle([
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('TOPPADDING', (0, 0), (-1, -1), 4),
            ('BOTTOMPADDING', (0, 0), (-1, -1), 8),
        ]))
        story.append(charts_tbl)
        story.append(Spacer(1, 12))

    # ─── Overall posture ───
    story.extend(section(S['sec_overall'],
                         prose(analysis.get('overall_posture'), styles) or
                         [Paragraph(esc(S['none']), styles['Body'])],
                         styles, width))

    # ─── CA / Intune / Security Settings ───
    for key, label in (
        ('conditional_access', S['sec_ca']),
        ('intune', S['sec_intune']),
        ('security_settings', S['sec_settings']),
    ):
        story.extend(section(label, render_area(analysis.get(key), styles, S), styles, width))

    # ─── Strengths summary ───
    if analysis.get('strengths_summary'):
        story.extend(section(S['sec_strengths'],
                             prose(analysis.get('strengths_summary'), styles),
                             styles, width))

    # ─── Priority actions ───
    action_flows = []
    actions = analysis.get('prioritized_actions') or []
    if actions:
        for idx, action in enumerate(actions, start=1):
            label, color = priority_label(action.get('priority'), S)
            head = (f'<b>{idx}.</b> <font color="{color}"><b>[{esc(label)}]</b></font> '
                    f'<b>{esc(action.get("title") or "")}</b>')
            if action.get('area'):
                head += f' <font color="{COLORS["text_light"]}">— {esc(action.get("area"))}</font>'
            block = [Paragraph(head, styles['Gap'])]
            if action.get('detail'):
                block.append(Paragraph(esc(action.get('detail')), styles['Gap']))
            tpl = action.get('deployable_template')
            if tpl:
                block.append(Paragraph(
                    f'&#10003; {esc(S["deployable"])} — {esc(S["tpl"])}: <b>{esc(tpl)}</b>',
                    styles['GapMeta']))
            else:
                block.append(Spacer(1, 6))
            action_flows.append(KeepTogether(block))
    else:
        action_flows.append(Paragraph(esc(S['none']), styles['Body']))
    story.extend(section(S['sec_actions'], action_flows, styles, width))

    # ─── Build: cover page first, body from page 2 ───
    doc = SimpleDocTemplate(
        output_path, pagesize=letter,
        leftMargin=0.75 * inch, rightMargin=0.75 * inch,
        topMargin=0.7 * inch, bottomMargin=0.8 * inch,
        title=f"{S['title']} — {tenant.get('display_name', '')}",
    )
    cover = make_cover(S, tenant.get('display_name', ''), data.get('generatedAt'), find_cover_image())
    footer = make_footer(S, msp_name, attribution_on)
    doc.build([Spacer(1, 1), PageBreak()] + story,
              onFirstPage=cover, onLaterPages=footer)
    print(f'Quick Assessment PDF written: {output_path}')


if __name__ == '__main__':
    main()
