def chart_score_gauge(score_pct, buf):
    """Donut gauge for the Microsoft Secure Score. Returns False if no data."""
    if score_pct is None:
        return False
    try:
        score = float(score_pct)
    except (TypeError, ValueError):
        return False

    fig, ax = plt.subplots(figsize=(2.6, 2.6))
    ring = COLORS['good'] if score >= 70 else COLORS['medium'] if score >= 45 else COLORS['high']
    ax.pie([score, max(0.0, 100 - score)], colors=[ring, '#E8E8E8'],
           startangle=90, counterclock=False,
           wedgeprops=dict(width=0.32, edgecolor='white', linewidth=2))
    ax.text(0, 0, f'{round(score)}%', ha='center', va='center',
            fontsize=26, fontweight='bold', color=COLORS['primary'])
    ax.set(aspect='equal')
    fig.savefig(buf, format='png', dpi=150, bbox_inches='tight',
                facecolor='white', edgecolor='none')
    plt.close(fig)
    return True


def chart_findings_donut(counts, buf, S):
    """Donut of findings by priority (high/medium/low). False if no findings."""
    order = [('high', S['prio_high'], COLORS['high']),
             ('medium', S['prio_medium'], COLORS['medium']),
             ('low', S['prio_low'], COLORS['low'])]
    values, colors_list = [], []
    for key, _label, color in order:
        cnt = counts.get(key, 0)
        if cnt > 0:
            values.append(cnt)
            colors_list.append(color)
    total = sum(values)
    if total == 0:
        return False

    fig, ax = plt.subplots(figsize=(2.6, 2.6))
    ax.pie(values, colors=colors_list, startangle=90, counterclock=False,
           wedgeprops=dict(width=0.32, edgecolor='white', linewidth=2))
    ax.text(0, 0.07, str(total), ha='center', va='center',
            fontsize=24, fontweight='bold', color=COLORS['primary'])
    ax.text(0, -0.20, S['findings_unit'], ha='center', va='center',
            fontsize=8, color=COLORS['text_light'])
    ax.set(aspect='equal')
    fig.savefig(buf, format='png', dpi=150, bbox_inches='tight',
                facecolor='white', edgecolor='none')
    plt.close(fig)
    return True


def prose(text, styles):
    """Split a narrative string on blank lines into separate paragraphs."""
    flows = []
    if not text:
        return flows
    for chunk in [c.strip() for c in str(text).replace('\r', '').split('\n\n') if c.strip()]:
        flows.append(Paragraph(esc(chunk).replace('\n', '<br/>'), styles['Body']))
    return flows


def section_header(label, styles, width):
    """A full-width colored section bar."""
    tbl = Table([[Paragraph(esc(label), styles['Section'])]], colWidths=[width])
    tbl.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), HexColor(COLORS['primary'])),
        ('LEFTPADDING', (0, 0), (-1, -1), 10),
        ('RIGHTPADDING', (0, 0), (-1, -1), 10),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]))
    return tbl


def section(label, content_flows, styles, width):
    """A section: the header bar is glued to its first content flowable via
    KeepTogether, so a header can never be orphaned at the foot of a page."""
    header = section_header(label, styles, width)
    if not content_flows:
        return [header, Spacer(1, 12)]
    head_group = KeepTogether([header, Spacer(1, 8), content_flows[0]])
    return [head_group] + content_flows[1:] + [Spacer(1, 12)]


def priority_label(priority, S):
    p = (priority or '').lower()
    if p == 'high':
        return S['prio_high'], COLORS['high']
    if p == 'low':
        return S['prio_low'], COLORS['low']
    return S['prio_medium'], COLORS['medium']


def gap_flowables(gap, styles, S):
    """The raw flowables for one finding — title + priority tag, detail, and
    the optional template line. Returned as a flat list so callers can wrap
    them in a single (non-nested) KeepTogether."""
    label, color = priority_label(gap.get('priority'), S)
    head = (f'<font color="{color}"><b>[{esc(label)}]</b></font> '
            f'<b>{esc(gap.get("title") or "")}</b>')
    out = [Paragraph(head, styles['Gap'])]
    if gap.get('detail'):
        out.append(Paragraph(esc(gap.get('detail')), styles['Gap']))
    tpl = gap.get('deployable_template')
    if tpl:
        out.append(Paragraph(
            f'&#10003; {esc(S["deployable"])} — {esc(S["tpl"])}: <b>{esc(tpl)}</b>',
            styles['GapMeta']))
    else:
        out.append(Spacer(1, 4))
    return out


def render_area(area, styles, S):
    """Render a CA / Intune / Security-Settings section's content flowables.
    Each finding is one atomic KeepTogether and the "Gaps" sub-heading is
    flat-glued to the first finding — no nested KeepTogethers, which would
    mis-measure and strand whitespace at page ends."""
    flows = []
    flows.extend(prose((area or {}).get('narrative'), styles))

    strengths = (area or {}).get('strengths') or []
    if strengths:
        bullets = [
            Paragraph(f'<font color="{COLORS["good"]}">&#10003;</font> {esc(item)}', styles['Bullet'])
            for item in strengths
        ]
        flows.append(KeepTogether([Paragraph(esc(S['sub_working']), styles['SubHead']), bullets[0]]))
        flows.extend(bullets[1:])

    gaps = (area or {}).get('gaps') or []
    gaps_subhead = Paragraph(esc(S['sub_gaps']), styles['SubHead'])
    if gaps:
        # First finding flat-glued to the sub-heading; each finding atomic.
        flows.append(KeepTogether([gaps_subhead] + gap_flowables(gaps[0], styles, S)))
        for gap in gaps[1:]:
            flows.append(KeepTogether(gap_flowables(gap, styles, S)))
    else:
        flows.append(KeepTogether([gaps_subhead, Paragraph(esc(S['none']), styles['Body'])]))
    return flows


